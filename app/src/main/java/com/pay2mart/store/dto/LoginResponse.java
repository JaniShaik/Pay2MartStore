package com.pay2mart.store.dto;

import android.os.Parcel;
import android.os.Parcelable;

import com.google.gson.annotations.SerializedName;

import java.util.ArrayList;

/**
 * Created by Jani Shaik on 08-Jul-21
 */

public class LoginResponse implements Parcelable {

    @SerializedName("status")
    private String status;

    @SerializedName("message")
    private String message;

    @SerializedName("data")
    private ArrayList<StoreDetails> listStoreDetails = new ArrayList<>();

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public ArrayList<StoreDetails> getListStoreDetails() {
        return listStoreDetails;
    }

    public void setListStoreDetails(ArrayList<StoreDetails> listStoreDetails) {
        this.listStoreDetails = listStoreDetails;
    }

    protected LoginResponse(Parcel in) {
        status = in.readString();
        message = in.readString();
        listStoreDetails = in.createTypedArrayList(StoreDetails.CREATOR);
    }

    public static final Creator<LoginResponse> CREATOR = new Creator<LoginResponse>() {
        @Override
        public LoginResponse createFromParcel(Parcel in) {
            return new LoginResponse(in);
        }

        @Override
        public LoginResponse[] newArray(int size) {
            return new LoginResponse[size];
        }
    };

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel parcel, int i) {
        parcel.writeString(status);
        parcel.writeString(message);
        parcel.writeTypedList(listStoreDetails);
    }
}
